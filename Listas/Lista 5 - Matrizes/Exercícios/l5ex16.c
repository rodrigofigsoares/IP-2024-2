#include <stdio.h>

#define N 100

void mapa(int M[][6]) {
    M[0][0] = M[1][1] = M[2][2] = M[3][3] = M[4][4] = M[5][5] = 0;
    M[0][1] = M[1][0] = 63;
    M[0][2] = M[2][0] = 210;
    M[0][3] = M[0][5] = M[3][0] = M[5][0] = 190;
    M[0][4] = M[5][2] = -1;
    M[2][1] = M[1][2] = 160;
    M[3][1] = M[1][3] = 150;
    M[4][1] = M[1][4] = 95;
    M[2][3] = M[1][5] = M[3][2] = M[3][4] = M[2][5] = M[4][0] = 10;
    M[2][4] = 1;
    M[3][5] = 20;
    M[4][2] = 7;
    M[4][3] = 21;
    M[5][4] = M[4][5] = 80;
    M[5][1] = 2;
    M[5][3] = 41;
}

int main() {
    int M[6][6], v[N];
    mapa(M);
    int n, i, soma = 0;
    scanf("%d", &n);
    for(i=0;i<n;i++) scanf("%d", &v[i]);
    for(i=0;i+1<n;i++)
    {
        if(M[v[i]][v[i+1]] == -1)
        {
            printf("rota invalida!\n");
            return 0;
        }
        soma += M[v[i]][v[i+1]];
    }
    printf("%d\n", soma);
    return 0;
}